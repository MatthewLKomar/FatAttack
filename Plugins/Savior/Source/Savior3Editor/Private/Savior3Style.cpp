#include "Interfaces/IPluginManager.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define PLUGIN_BRUSH(RelativePath,...) FSlateImageBrush(FSavior3Style::InContent(RelativePath,".png"),__VA_ARGS__)

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

TSharedPtr<FSlateStyleSet> FSavior3Style::StyleSet = nullptr;
TSharedPtr<ISlateStyle> FSavior3Style::Get() {return StyleSet;}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

FString FSavior3Style::InContent(const FString &RelativePath, const ANSICHAR* Extension) {
	static FString Content = IPluginManager::Get().FindPlugin(TEXT("Savior"))->GetContentDir();
	return (Content/RelativePath)+Extension;
}

FName FSavior3Style::GetStyleSetName() {
	static FName StyleName(TEXT("Savior3Style"));
	return StyleName;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void FSavior3Style::Initialize() {
	if (StyleSet.IsValid()) {return;}
	//
	const FVector2D Icon16x16(16.0f,16.0f);
	const FVector2D Icon128x128(128.0f,128.0f);
	//
	StyleSet = MakeShareable(new FSlateStyleSet(GetStyleSetName()));
	//
	StyleSet->Set("ClassIcon.Savior3", new PLUGIN_BRUSH(TEXT("Icons/Savior_16x"),Icon16x16));
	StyleSet->Set("ClassThumbnail.Savior3", new PLUGIN_BRUSH(TEXT("Icons/Savior_128x"),Icon128x128));
	//
	FSlateStyleRegistry::RegisterSlateStyle(*StyleSet.Get());
};

void FSavior3Style::Shutdown() {
	if (StyleSet.IsValid()) {
		FSlateStyleRegistry::UnRegisterSlateStyle(*StyleSet.Get());
		ensure(StyleSet.IsUnique());
		StyleSet.Reset();
	}///
}

