USavior3Factory::USavior3Factory(const class FObjectInitializer &OBJ) : Super(OBJ) {
	bCreateNew = true;
	bEditAfterNew = true;
	SupportedClass = USavior3::StaticClass();
}

UObject* USavior3Factory::FactoryCreateNew(UClass* Class, UObject* InParent, FName Name, EObjectFlags Flags, UObject* Context, FFeedbackContext* Warn) {
	check(Class->IsChildOf(USavior3::StaticClass()));
	return NewObject<USavior3>(InParent,Class,Name,Flags,Context);
}

FText FATA_Savior3::GetAssetDescription(const FAssetData &AssetData) const {
	return FText::FromString(FString(TEXT("Savior Slot.\nObject holding Record Data from Game World for the 'Auto-Save System'.")));
}

