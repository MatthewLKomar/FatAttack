#include "ISavior3Editor.h"
#include "Savior3Editor.h"
#include "Savior3Style.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define LOCTEXT_NAMESPACE "Synaptech"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class FSavior3Editor : public ISavior3Editor {
public:
	virtual void StartupModule() override {
		IAssetTools& AssetTools = FModuleManager::LoadModuleChecked<FAssetToolsModule>("AssetTools").Get();
		SY_AssetCategory = AssetTools.RegisterAdvancedAssetCategory(FName(TEXT("Synaptech")),LOCTEXT("SynaptechCategory","Synaptech"));
		//
		{
			TSharedRef<IAssetTypeActions> ATA_Savior3 = MakeShareable(new FATA_Savior3);
			AssetTools.RegisterAssetTypeActions(ATA_Savior3);
		}
		//
		FSavior3Style::Initialize();
	}
};

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#undef LOCTEXT_NAMESPACE

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

IMPLEMENT_MODULE(FSavior3Editor,Savior3Editor);

